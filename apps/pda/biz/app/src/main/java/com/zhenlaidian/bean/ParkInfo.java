package com.zhenlaidian.bean;

public class ParkInfo {

	private String parkingtotal;
	private String phone;
	private String parktype;
	private String price;
	private String address;
	private String name;
	private String service;
	private String stoptype;
	private String timebet;
	public ParkInfo() {
		super();
	}
	public String getParkingtotal() {
		return parkingtotal;
	}
	public void setParkingtotal(String parkingtotal) {
		this.parkingtotal = parkingtotal;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getParktype() {
		return parktype;
	}
	public void setParktype(String parktype) {
		this.parktype = parktype;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getService() {
		return service;
	}
	public void setService(String service) {
		this.service = service;
	}
	public String getStoptype() {
		return stoptype;
	}
	public void setStoptype(String stoptype) {
		this.stoptype = stoptype;
	}
	public String getTimebet() {
		return timebet;
	}
	public void setTimebet(String timebet) {
		this.timebet = timebet;
	}
	
	
	
	
}
